function getFlagsUrl(value) {
  let toReturn = `assets/No_flag.svg.png`;
  if (navigator.onLine) {
    toReturn = `https://www.countryflags.io/${value}/shiny/64.png`;
  }
  return toReturn;
}
function getCurrentTimeAndDate(timestamp) {
  let myDate = new Date(timestamp * 1000);
  return myDate.toLocaleString();
}

